<div class="container-fluid bg-2 text-center">
 
  <p>Posyantek as institution which built by government is Pos Pelayanan Teknologi at the sub district level to give technical service, information, promotion and marketing for technology device specification which will be needed by public and for escalating human resources skill at rural area (Kepmendagri No.18 tahun 1992 tentang Pemasyarakatan dan Pemanfaatan Teknologi Tepat Guna di Pedesaan). This institution existence is already a lot and no exception for Posyantek Kecamatan Tegal Timur. But this institution has not optimal for its management, so this institution is name only, function revitalization and role for this institution are very needed, one of Posyantek function is as Center of Technical Service Information, Promotion and Marketing and has one purpose to developed and spread to public and make it easier for pubic to develop business with efficient technology. Method which will be used to build this system is Object Oriented Analysis and Design (OOAD) method. This method results system model for requirement stage, analysis and next design. Target results on this research is system information built as information media and marketing media will help function effectivity and purpose Posyantek built with marketing information address with web basic http://posyantek.wordpress.com.<br>
<b><i>Keyword: Marketing Information System, Object Oriented Analysis and Design (OOAD).</b></i></p>
