 <h3>Abstract</h3>
  <h3>Teacher Discussion Forum Information Media Programming with Web Basic (Case Study in SMPN 2 Cineam Kabupaten Tasikmalaya) </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Information system at SMPN 2 Cineam still does manually. In data processing and information delivery still conventional doing. With current system many deficieny is still felt. 
This final thesis purpose is to build information media with web basic in the form of discussion forum on SMPN 2 Cineam to facilitate and make it easier on information delivery both academic or non academic, where student and teacher can gain school information easier and faster. Other than that with web basic, information can be accessed by special user such as student, teacher and administrator.
In this application establishment, author are using PHP and MySQL. Author analyze this information media too if this thing can help school on studying process and information delivery, both school administration, teacher or student.
<br>
<b><i>Keyword: SMPN 2 Cineam, Information Media with Web Basic, Discussion Forum, PHP and MySQL.</b></i></p>
