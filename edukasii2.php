 <h3>Abstract</h3>
  <h3>Interactive Mandarin Language Education System Programming with Open Source E-Learning Basic </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>This day Mandarin language is demanded by public. In Indonesia, international language which often used after English is Mandarin (Sylvie Tanaga, 2010). Mandarin language as a subjects majoring in language on 2013 curriculum which can be learned SLTA Student. Mandarin language characteristics is unique both alphabet, text, voice and its intonation, its study process is more interactive, supported with multimedia device and doesn’t always in a class. Mandarin language studying alternatives will be more interesting with interactive play and supported by offline/online education facility which can be accesed by information technology device. Student chance using computer or internet, make it easier to communicate online with their friends or teachers, or other people to understand more about the Mandarin language. To fullfill that needs, software application must be designed as Mandarin language education media. Education system application built programming can be developed with the use of free open source e-learning application package. The programming method with life cycle development system approach can be reached through some of sequences start from planning, analyzing, programming, until  application education system which can be accesed through website. The outcome from research results is e-learning system, which hopefully can become education media alternative for Mandarin language.<br>
<b><i>Keyword: Education, Interactive, E-Learning, Open Source.</b></i></p>
