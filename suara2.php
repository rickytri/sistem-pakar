<div class="container-fluid bg-2 text-center">
 
  <p>Perkembangan ilmu pengetahuan dan teknologi
khususnya di bidang robotika membawa dampak positif
dalam kehidupan manusia yang pada saat ini telah
sampai pada zaman autonomous robot. Perkembangan di
bidang robotika ini juga mampu memberikan dampak
positif di bidang industri. Salah satu contohnya adalah
proses memindahkan sebuah barang/objek dari suatu
tempat ke tempat lainnya. Penggunaan mobile robot yang
dapat memindahkan barang/objek dapat menekan biaya
produksi dan meningkatkan efisiensi waktu. Dalam
kenyataannya, sering dijumpai robot pemindah
barang/objek yang salah satu sistem kerjanya adalah
dengan cara memberi perintah pada mobile robot tersebut
menggunakan lebih dari satu tombol atau dalam bentuk
joystick. Oleh karena itu, dalam skripsi ini akan
dirancang Implementasi Sistem Voice Recognition pada
Robot Pemindah Objek sebagai Sistem Navigasi. Metode
pengambilan sample suara pada modul EasyVR dilakukan
sebanyak dua kali pengambilan suara dengan variasi
pengucapan yang relatif sama pada setiap kata. Hal ini
dilakukan sesuai dengan kemampuan EasyVR yang tidak
bisa menerima pengucapan variasi suara kedua jika
berbeda dengan variasi pengucapan suara pertama.
Selain itu, EasyVR commander memberi batas waktu
selama 5 detik untuk setiap pengucapan suara yang akan
dijadikan sample. Penerapan sistem voice recognition
pada robot pemindah objek dapat digunakan sebagai
sistem navigasi robot meskipun belum mampu
menggantikan penggunaan perangkat berupa joystick.<br>
<b><i>Kata Kunci: sistem voice recognition, EasyVR, robot
pemindah objek.
</b></i></p>
