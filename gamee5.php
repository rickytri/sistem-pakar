<div class="container-fluid bg-2 text-center">
 
  <p>Indonesia is one country with a history of struggle for independence long enough number
of fighters and national figures who have fallen in battle and the amount of the value of
history and the struggles of the fighters we're all more or less already started to be
forgotten, especially among children in the current era many android based games, with
the development of smartphone and tablet users in the world. Industry game makers also
have many opportunities to produce games. Many of the benefits of producing the game
because of the many enthusiasts of the game. In this paper the author will design and build
applications gaming history of the struggle of national heroes Kapitan Pattimura android
based in the design of this game using Adobe Flash CS6 which can then be run on android
Smartphone. In this game later on the user or the user will play the character of a hero
Kapitan Pattimura to fight its wars against the Dutch and the future until the end of
powers to seize the occupied Netherlands. In the making of this game will use multimedia
development method development, Steps in the research process is the stage of concept,
design, collecting materials, assembly, testing, distribution.<br>
