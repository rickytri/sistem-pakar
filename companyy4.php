<div class="container-fluid bg-2 text-center">
 
  <p>Multimedia development is running very fast along with computer technology development. Multimedia is widely used on business world, as an example for advertising, education, company profile establishment, presentation media, broadcast, etc. Multimedia technology is a technology which integrate computer with variety of interaction media to produce interactive and interesting application system. Multimedia using can make new innovation in producing package dynamic data and information with different way, not only with static text but with text animation, image, voice, and video. Multimedia implementation is done to help promote the company. One of the implementation is company profile application. The purpose of multimedia basic company profile application is to help producer promote the company, where this application in the future can be used as media/business presentation equipment and can facilitate consumer to know information about the company.
Research methods which have been used on multimedia basic company profile application media are data collection method which consist of literature study and observation method, and multimedia application development method which consist of concept, design, material collecting, producing, program test, and distribution (implementation). With this multimedia basic company profile, hopefully it can help PT. Mesana Group to compete with another typical company to gain consumer.

