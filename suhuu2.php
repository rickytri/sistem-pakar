 <h3>Abstract</h3>
  <h3>Design of Human Body's Temperature Monitoring Prototype Based on Android Using Bluetooth Connection </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Vital signs of human health can be known from body temperature, respiratory, blood pressure, and pulse. This final project intends to designs a human's body temperature monitoring prototype based on android using Bluetooth connection in form of a three points of measurement jacket. 
The steps are designing it's hardware and sofware systems.  The sensor is IC-LM35 which located in three measurement points. 
This sensor will be act as the detector of body's temperature distinction, then the result will be read and processed on ATMega8a-pu microcontroller. 
After the data processed, then the data will be sent to android device via bluetooth connection. 
After measurement and data analysis, it can be seen that this tool has a dynamic settling time response of 5 minutes. Maximum distance of device connection is 35 meters, and capability of 9 volts battery for 9 volts for 90 minutes use. 
This tool has an error accuracy value of 0.29150C, precision error of 0.8 ° C for Sensor A (mouth), and an accurate error value of 0.4149 ° C, error
precision of 0.2974 ° C for sensor B (right armpit), and error accuracy value of 0.3846 ° C, precision error of 0.5956 ° C for sensor C (left armpit).
<br>
<b><i>Keywords : temperature, health, microcontroller, bluetooth,Android.
</b></i></p>
