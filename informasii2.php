 <h3>Abstract</h3>
  <h3>Information System Programming with Web Basic as Information Media and Promotion for MM Music Studio Banjarnegara</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Website is one of the Internet technology used to process the information. A
website allows users to interact and find information on the internet. In the preparation of
this thesis website created for a music company, the MM Studio Music Banjarnegara.
This thesis is titled "Designing Web-Based Information System For Media
Information and Promotion Of MM Studio Music Banjarnegara ". Designing websites
using Adobe Dreamweaver CS3 for coding editor, Adobe Photoshop CS3 to design the
appearance and image, as the XAMPP web server package and database, for coding
programming using PHP, CSS and Java Script.
With the MM Studio Music Banjarnegara website is expected to facilitate the MM
studio in providing information about the company profile as well as promote the general
public and especially for people who have a hobby of music in Banjarnegara.
<br>
<b><i>Keywords: website, mm studio musik, banjarnegara, information system.</b></i></p>
