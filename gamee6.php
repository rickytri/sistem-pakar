 <h3>Abstract</h3>
  <h3>Mathematic Maze Education Games Built with Android Basic to Accelerate Calculated Skill for Elementary School Children</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>The development of technology for today is very fast with the increasing number of applications of
learning that is generated for smartphones and tablet devices. One of these applications is currently
developing learning such as educational games. Mathematic Maze Game based on android is made for
children who are still in Elementary School to improve numeracy skills basic mathematical operations.
This application is built using Adobe Flash CS 6 with Action Script 3.0 as the programming language and
Corel Draw X7 as the design layout. This application is built with some animation and sound features that
can help users to play while learning mathematics. From the results of tests performed by the authors of
the applications that are built stating that there is no error when the application runs on the android
operating system version of Lollipop. This application can be operated at a minimal version of the
android Ice Cream Sandwith until Kitkat version, but there is an error in the navigation button sometimes
does not work. Interface is right for this application is on the smartphone device with a screen size of 6
inches. This application was created with the concept of challenge and speed of thinking so that users of
these applications are challenged to achieve success playing. The challenge of the game is to answer basic
math problems such as addition, subtraction, multiplication, and division of a predetermined time. This
application has a different size after installed on the device version android depending on the device.<br>
<b><i>Keywords: Game, Mathematics, Maze, Adobe Flash</b></i></p>
