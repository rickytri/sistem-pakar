<div class="container-fluid bg-2 text-center">
 
  <p>Perkembangan teknologi untuk saat ini sangat cepat dengan semakin banyaknya aplikasi
tentang pembelajaran yang dihasilkan untuk perangkat smartphone dan tablet. Salah satunya aplikasi
pembelajaran yang berkembang saat ini seperti game edukasi. Game Mathematic Maze berbasis android
ini dibuat untuk anak yang masih dibangku Sekolah Dasar untuk meningkatkan kemampuan berhitung
operasi matematika dasar. Aplikasi ini dibangun menggunakan Adobe Flash CS 6 dengan Action Script
3.0 sebagai bahasa pemrogramannya dan Corel Draw X7 sebagai desain layoutnya. Aplikasi ini dibangun
dengan beberapa fitur animasi dan suara yang dapat mempermudah pengguna dalam bermain sambil
belajar matematika. Dari hasil pengujian yang dilakukan oleh penulis terhadap aplikasi yang dibangun
menyatakan bahwa tidak ada kesalahan ketika aplikasi dijalankan pada sistem operasi android versi
Lollipop. Aplikasi ini dapat dioperasikan pada android versi minimal Ice Cream Sandwith sampai versi
Kitkat namun ada error pada tombol navigasi yang terkadang tidak berfungsi. Interface yang tepat untuk
aplikasi ini yaitu pada perangkat smartphone dengan ukuran layar 6 inch. Aplikasi ini dibuat dengan
konsep tantangan dan kecepatan berfikir sehingga pengguna aplikasi ini merasa tertantang untuk
mencapai keberhasilan bermain. Tantangan dari permainan ini adalah menjawab soal matematika dasar
berupa penjumlahan, pengurangan, perkalian, dan pembagian waktu yang telah ditentukan. Aplikasi ini
mempunyai ukuran yang berbeda-beda setelah diinstal pada perangkat android tergantung versi android
pada perangkat tersebut.<br>
<b><i>Kata Kunci: Game, Matematika, maze, adobe flash</b></i></p>
