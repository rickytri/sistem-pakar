<div class="container-fluid bg-2 text-center">
 
  <p>Salah satu jenis robot dengan kemampuan istimewa yang menarik untuk dikembangkan adalah robot
beroda dengan pengendali suara yang memanfaatkan teknologi pengenalan suara sebagai penggerak motor DC.
Perancangan robot beroda dengan pengendali suara ini menggunakan Arduino UNO R3 sebagai pengontrol, modul
Easy Voice Recognition, Motor Driver sebagai pengendali motor DC, dan untuk mekaniknya menggunakan sistem
roda dengan motor DC sebagai aktuatornya. Tugas akhir ini menggunakan C++ Arduino sebagai bahasa
pemrograman, Easy Voice Commander sebagai perekam suara, dan Matlab sebagai pengolah sinyal suara dan untuk
melihat perbedaan antara sinyal suara yang berhasil dikenali dan gagal dikenali. Keberhasilan pengujian pengenalan
kata pada kondisi lingkungan ideal adalah 85% , pada kondisi lingkungan berderau adalah 40%, sedangkan
keberhasilan pengujian kinerja sistem pada kondisi lingkungan ideal adalah 87,5%, pada kondisi lingkungan
berderau 37,5%.<br>
<b><i>Kata Kunci : Arduino UNO R3, Matlab, Pengenalan Suara, Robot
</b></i></p>
