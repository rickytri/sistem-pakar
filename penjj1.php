 <h3>Abstract</h3>
  <h3>Batik Sales Application Programming with Android Basic </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Technology development is more increasing pushing human to utilize and use technology as well as possible. Developed electronic device type and applications appearance and nowadays internet can be enjoyed wherever. Other than that mobile operation system appearance which can be called Android has decent performance with smartphone for phone which using that operation system. With faster development era can be used to advertising media and information media to introduce and make wide network for Batik Puspa Kencana. From this case, I initiated to make a Batik Sales Application with Android Basic (case study at Batik Puspa Kencana Laweyan, Solo) which hopefully can help in introduction, sell-buy media, and can open wide network for Batik Puspa Kencana.
This application consists of informations about Batik Puspa Kencana, which products, and there is online sell-buy media which can be used by user to order batik products on Batik Puspa Kencana.
The used method in this application establishment is with SDLC (System Development Life Cycle) Waterfall method. Then programming will be done with UML process (Unfied Modelling Language) with diagram use case, flowchart than applied in application using Ecplise and ADT (Android Development Tool) for making application with android basic.
The gain results based on questionnaire which be done to 11 respondent whom consists of 3 seller and 8 buyer through application so it had Interpretation Percentage 92% from seller respondent and 87% buyer respondent. So the average of interpretation percentage is 89,5% that made application can be useful to help sales system at Batik Puspa Kencana.
<br>
<b><i>Keywords: Android, Sales, Batik, Information Technology.</b></i></p>
