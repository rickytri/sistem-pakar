  <p>
Company Profile as a data or profile a company or institution, which can be used to introduce business to colleague. Company profile is not only needed by company which is an office only, but can be needed too by commerce company such as restaurant and cafe.
Company profile can be used to attract customer to come and know its business.  Company profile have 2 shapes, print company profile and web basic company profile. Company profile is not used only to introduce company profile, but for commercial purpose,  company profile is an effective media as a promotion or marketing, especially through web. As an example Lemon Tea Restaurant. Lemon Tea Restaurant is a restaurant which can be categorized as newbie, so it needs web basic company profile as a promotion. Web basic company profile can be accessed everywhere, whenever, and not knowing boundary space, so it can reach a lot of circles. Research which have been made is "Web Basic Lemon Tea Restaurant’s Company Profile Programming as Communication Media and Promotion". Datas to this company profile are gained through direct observation and interview. This web basic company profile is expected to introduce Lemon Tea Restaurant to netizen, develop the business and escalate customer quantity, quality, and image from Lemon Tea Restaurant.
