<div class="container-fluid bg-2 text-center">
 
  <p>Kemajuan pendidikan akan berpengaruh secara signifikan terhadap kemajuan
suatu bangsa, khususnya kemajuan ilmu pengetahuan dan teknologi. Upaya
penerapan teknologi khususnya teknologi informasi komunikasi dibidang pendidikan
salah satunya adalah sistem pembelajaran menggunakan media internet atau biasa
disebut dengan e-learning. Dengan adanya e-learning memberi kemudahan dalam
mendapatkan materi pembelajaran tanpa berbayar, mengasah kemampuan dengan
latihan-latihan soal dan lebih mudah berinteraksi dengan pengajar dimanapun dan
kapanpun.
Metode pengembangan yang digunakan penulis adalah metode waterfall
dalam melakukan penelitian ini. Metode waterfall yaitu metode yang menggunakan
pendekatan secara sistematis dan urut mulai dari level mendefinisikan kebutuhan
sistem sampai maintenance. Dimana metode waterfall memiliki 6 fase, yaitu 
Requirement Specification, Architectural Design, Coding, Integration & Testing,
Operation & Maintenance.
Hasil dari penelitian adalah lebih dari 80% responden menyatakan sistem ini
mudah digunakan, sistem ini memiliki menu yang lengkap, dan sistem ini dapat
digunakan untuk belajar mandiri dirumah, serta lebih dari 75% responden
menyatakan sistem ini dapat meningkatkan kualitas belajar. Dengan kesimpulan
tersebut maka tujuan dari penelitian ini telah tercapai.<br>
<b><i>Kata kunci : Sistem Pembelajaran, E-learning, Web</b></i></p>
