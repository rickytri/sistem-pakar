<div class="container-fluid bg-2 text-center">
 
  <p>Alat musik tradisional rebana merupakan alat musik tradisional yang biasanya digunakan untuk
pengiring acara-acara Islami. Meskipun alat musik tradisional rebana bukan asli dari Indonesia, namun
pada penyebarannya, alat musik ini memiliki ciri khas dari daerah masing-masing. Namun seiring
perkembangan jaman, alat musik ini cenderung ditinggalkan. Masyarakat pada umumnya cenderung
lebih suka terhadap alat musik modern. Hal ini didukung dengan minimnya game-game tentang alat
musik tradisional, sedangkan game-game virtual untuk alat musik tradisional sangatlah banyak. Device
berbasis Android pada era sekarang sangatlah berkembang pesat, baik dari sudut pandang pengembang
maupun pengguna. Pembuatan game ini menggunakan metode prototype. Game Pembelajaran Alat
Musik Tradisional Rebana berbasis Android ini bertujuan untuk membuat suatu game pembelajaran
memainkan alat musik tradisional rebana secara mobile berbasis android, selain itu juga memberikan
pengetahuan tentang alat musik rebana, cara bermain dan informasi tentang alat musik rebana tersebut.
Diciptakannya dalam bentuk mobile bertujuan supaya mudah untuk dibawa kemana saja. Dengan
diciptakannya game ini memungkinkan pengguna untuk berinteraksi secara langsung dalam proses
bermain rebana sesuai dengan mekasnisme alat musik yang sesungguhnya. Hasil survei yang ditujukan
kepada 10 orang dari beberapa kalangan, didapat tingkat kepuasan sebesar 82,4 % yang berarti sangat
puas.<br>
<b><i>Kata kunci: game, alat musik tradisional, rebana, Android.</b></i></p>
