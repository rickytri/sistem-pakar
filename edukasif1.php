  <p>Pendidikan Prasekolah sangat berperan besar dalam mendukung pertumbuhan dan perkembangan
jasmani dan rohani anak didik sebelum memasuki jenjang sekolah dasar. Salah satu alternatif
untuk mendukung meningkatkan pembelajaran prasekolah tersebut adalah dengan memanfaatkan
perangkat mobile Android yang saat ini sedang berkembang pesat. Aplikasi ini menggunakan dasar
pemrograman Adobe ActionScript 3.0 pada Flash dengan OS android yang digunakan Froyo (2.2)
keatas. Pembelajaran pada sistem ini dimulai dengan menampilkan pilihan menu pembelajaran
abjad, angka, warna, bentuk, suara hewan, dan menggambar. Kemudian sistem menampilkan
pembelajaran sesuai dengan pilihan yang dilakukan oleh user. Sistem menampilkan pembelajaran
dengan dukungan gambar disertai suara yang menjelaskan materi pembelajaran yang sedang
ditampilkan. Aplikasi preschool basic berbasis Android ini dapat memberikan manfaat bagi anak- anak prasekolah.<br>
<b><i>Kata kunci: ActionScript 3.0, Android, Flash, mobile, Prasekolah.</b></i></p>
