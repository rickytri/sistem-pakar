<h3>Abstract</h3>
  <h3>Web Based Sales Information System with RAD Methodology (Case Study: PT SINTEX MECHATRONIC INDOJAYA) </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>PT SIMTEX Mechatronic Indojaya (SMI) is a hardware trading company which provides solutions in air circulation, vibration control and noise control. 
They also provide maintenance services for those items. 
However, in its sales activities PT. SMI still uses old ways like how to spread the the brochures, door to door, and by telemarketing by phone.
Those uneffective sales method, can't get the sales target as the company expected hardware, even worse it's become degrading each month.
This can be seen in Appendix II. Sales data management is also not using computerized system, used the computerized way, so that many data not well organized.
Lost invoices is also one of the factors that causing sales-marketing division can't get accurate  data about the most requested dan most sold items.
To solve those problems, writer proposes a system that can help PT. SMI to offer their product via internet and also more efficient data management.
This system using PHP and MYSQL as the database. This system development using onject oriented Rapid Application Development (RAD) and Microsoft Visio 2003  as the system design editor. The expected result by this system is to improving sales and data management, so it can be more efficient.
<br><br><br>
<b><i>Keywords: Sales Information System, Web, RAD</b></i></p>
