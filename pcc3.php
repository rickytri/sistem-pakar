 <h3>Abstract</h3>
  <h3>Public Monitoring System Model Development Using Raspberry Pi </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Limitations of the data processing device is large enough shape so it is less flexible in
mobile functionality. Raspberry Pi is a single board computer (single-board computer) or a credit
card-sized SBC. Raspberry Pi has been equipped with all functions like a complete computer,
using a SoC (System-on-a-chip) ARM packaged and integrated on the PCB. CCTV is one
device that is expected to reduce the crime rate in public areas. But the high cost of installation
constraints (either electrical or network) and limited mobility placement tool makes use of the
device is not maximal yet. This study offers an alternative solution to fix the above problems.
Prototype Development of Public Monitoring System Using a Raspberry Pi is an initial sub-study
of grand research Intelligent Application Integration Public Monitoring System. The output will be
achieved is to build a device for monitoring systems that have mobility design that can be
placed in various places. With this research is expected to provide the use of Raspberry Pi
discourse to establish a monitoring system that is inexpensive and power-efficient government
support programs.<br>
<b><i>Keywords : raspberry pi, monitoring system, cctv, sistem cerdas, mobility</b></i></p>
