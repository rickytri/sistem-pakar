  <p>Aplikasi Word Puzzle Game Berbasis Android adalah sebuah aplikasi permainan yang berjenis Word Puzzle yang berfungsi untuk
menghibur dan juga memberi pengetahuan mengenai nama-nama hewan dalam bahasa Inggris beserta dengan penjelasannya. Aplikasi
ini dibuat dengan menggunakan bahasa pemrograman Java dan menggunakan database Sqlite. Pengembangan dilakukan
dengan Eclipse sebagai alat utama, dan Adobe Photoshop sebagai alat penunjang. Proses pembuatan seluruh mekanisme dalam
permainan dilakukan dengan Eclipse, sedangkan proses desain tampilan dan objek dilakukan dengan Adobe Photoshop, dan proses
pengujian dilakukan pada smartphone Lenovo A800. Pada aplikasi ini terdapat dua fungsionalitas utama yakni bermain dan
melihat informasi. Pada fungsionalitas pertama pengguna akan diajak untuk memperbaiki nama-nama hewan yang typo. Selain itu,
pemain juga akan diajak untuk membuka achievement sehingga pengguna bisa melihat informasi hewan-hewan berupa gambar dan
juga penjelasannya itu semua termasuk ke dalam dungsionalitas kedua.<br>
<b><i>Kata Kunci: Permainan, Word Puzzle, Android.</b></i></p>
