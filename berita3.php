olahraga, teknologi, pendidikan dan hiburan. Berita yang ditampilkan mengambil dari rss
website portal berita lokal Indonesia. Pengguna bisa melihat detail berita dan membagikan
berita melalui situs jejaring sosial facebook dan twitter. Selain itu ada juga fitur pencarian berita
