 <h3>Abstract</h3>
  <h3>M-Commerce Application on FANI BAGS Store Android Based</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Bags Fani store is one bag shop located in BTC lt 1 Pangkalpinang Bangka Belitung. Fani Bags store sells a
wide range of bags such as backpacks, travel bags, briefcases etc. However, the mechanism of buying and selling or
marketing is done manually, the buyer must come to the store to buy goods from Fani Bags. Then it becomes
inefficient. M-commerce (mobile commerce) is a trading system that is performed by the method of trading is done
with portable media or mobile devices such as smartphones, PDAs etc.
This application is based Mobile Application (Android) using java programming and MySQL as the database
and PHP as webserver. M-commerce to be a solution to solve the problems on the Fani Bags store due to mcommerce
and smart phones as a media liaison, customers can easily access or purchase products Fani Bags store
without having to come to the store.
It can be concluded using mobile commerce applications of this transaction will be carried out more easily,
efficiently, and of course the wider area coverage.
<br>
<b><i>Keywords: M - Commerce , Android , Mobile Application, Fani Bags Store</b></i></p>
