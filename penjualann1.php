  <h3>Web Based Sales Application at PT. PRATAPA NIRMALA PALEMBANG </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>The purpose of writing this final project is to provide an overview of the running system and create a  web based sales application at PT. Pratapa Nirmala (Palembang branch). The method that used in this Final Project is Iterative methodology. 
The design is by making Use Case diagram, ERD, Sequence Diagram, File Specification, input design and outbound design and screen dialog design. 
This application built using PHP programming language and MySQL database. 
The result of this paper is to create an application which can help customer to get information and order goods from PT. Pratapa Nirmala (Palembang branch).
<br>
<b><i>Keywords : Web, Application, sales, Itterative, PHP, MySQL</b></i></p>
