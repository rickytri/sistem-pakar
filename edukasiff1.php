<div class="container-fluid bg-2 text-center">
 
  <p>Preschool play a huge role in supporting the growth and development of the students physically
and mentally before entering primary school level. One of the alternative to support improved
learning preschool is to use the Android mobile devices that are currently being developed
rapidly. This application uses the ActionScript 3.0 as the basic programming in Adobe Flash with
use the android OS Froyo (2.2) and above. Learning in this system begins with a menu choice of
learning the alphabet, numbers, colors, shapes, animal sounds, and drawing. Then the system
displays the learning according to the choices made by the user. To support learning, the system
displays the image with sound that describes material that is being displayed. This application is
expected to provide benefits for preschoolers. that this application has a very suitable material to
support the learning of preschool children.<br>
<b><i>Keywords: ActionScript 3.0, Android, Flash, mobile, Preschool</b></i></p>
