aplikasi yang bersifat permainan bergerak. Beragam aplikasi telepon telah banyak dimainkan,
terutama oleh anak-anak dan sebagian besar jenis permainan bergerak tersebut memberikan
kesan menghibur. Oleh karena itu, dirancanglah sebuah aplikasi permainan yang berjalan pada
telepon selular dan memberikan nilai edukasi dan juga bisa memberi hiburan. Perminan ini
berupa teka-teki silang yang didalamnya terdapat pertanyaan yang berada pada beberapa kotak
dari papan permainan. Dalam permainan ini, pemain harus dapat menjawab pertanyaan tersebut
yang pertanyaanya menggunakan bahasa inggris. Dimana pertanyaan yang keluar berupa soalsoal
yang telah di pilih pemain berdasarkan kategori yang ada. Dalam penelitian ini, penulis
merancang permainan berbasis mobile menggunakan java 2 micro edition (J2ME) dengan
melakukan langkah-langkah berikut yaitu mencari mengidentifikasi masalah , kajian pustaka ,
analisis kebutuhan yang di perlukan dan aplikasi,perancangan game, implementasi dan uji coba.<br>
