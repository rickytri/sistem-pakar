yang mengakses portal suaramerdeka.com melalui mobile browser sangatlah besar, dengan begitu itu perlu
dilakukan pengembangan ke arah aplikasi smartphone untuk mempermudah masyarakat luas dalam
mengakses berita secara cepat dan praktis. Dengan pendekatan teknologi diharapkan aplikasi berita mobile
pada suaramerdeka.com berbasis android ini dapat tersampaikan kepada pengguna dan dapat difungsikan
semaksimal mungkin untuk semua kalangan. Aplikasi ini dirancang mengikuti perkembangan teknologi
mobile saat ini yaitu teknologi android dengan alat bantu phonegap. Fitur utama aplikasi ini menyediakan
update berita terkini yang terjadi di dalam dan luar negeri, juga terdapat fitur khusus untuk mendapatkan
feedback dari pembaca berupa pelaporan suatu kejadian kepada pihak suarmerdeka. Dapat disimpulkan
bahwa aplikasi berita mobile pada suaramerdeka berbasis android ini telah memenuhi fitur utama dalam
penyampaian berita seperti yang terdapat pada portal berita suaramerdeka dan juga memenuhi kebutuhan
pengguna akan kemudahan untuk mengakses fasilitas portal berita suaramerdeka.<br>
