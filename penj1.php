  <h3>Perancangan Aplikasi Penjualan Batik Berbasis Android</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Perkembangan teknologi yang kian lama semakin meningkat mendorong sifat manusia
untuk memanfaatkan dan menggunakan teknologi dengan sebaik-baiknya. Munculnya berbagai
jenis perangkat elektronik dan aplikasi-aplikasi yang semakin berkembang serta berkembangnya
internet yang sekarang dapat di nikmati hampir bisa di mana saja. Selain itu munculnya sistem
operasi mobile yang bernama Android yang memiliki performa dan kinerja yang layak dengan
sebutan smartphone bagi ponsel yang menggunakan sistem operasi tersebut. Dengan
perkembangan jaman yang semakin pesat dapat dimanfaatkan untuk media periklanan dan media
informasi untuk mengenalkan dan membuat jaringan yang lebih luas bagi Batik Puspa Kencana.
Dalam hal ini saya berinisiatif untuk membuat sebuah Aplikasi Penjualan Batik Berbasis Android
(Studi Kasus di Batik Puspa Kencana Laweyan, Solo) yang di harapkan dapat membantu dalam
pengenalan, media jual-beli, serta dapat membuka jaringan yang lebih luas bagi Batik Puspa
Kencana.
Aplikasi ini berisi informasi-informasi tentang Batik Puspa Kencana, produk-produk yang
terdapat di sana, serta terdapat media jual-beli secara online yang dapat digunakan pengguna untuk
memesan produk-produk batik di Batik Puspa Kencana.
Metode yang digunakan dalam pembuatan aplikasi ini adalah dengan menggunakan metode
SDLC (System Development Life Cycle) Waterfall. Kemudian perancangan dilakukan dengan
proses UML (Unfied Modelling Language) dengan use case diagram, flowchart kemudian di
terapkan kedalam aplikasi menggunakan Eclipse serta ADT (Android Development Tool) untuk
pembuatan aplikasi berbasis android.
Hasil yang diperoleh berdasarkan kuisioner yang dilakukan kepada 11 responden yang
terdiri dari 3 penjual dan 8 pembeli terhadap aplikasi didapatkan Presentase Interpretasi sebesar
92% dari responden penjual dan 87% dari responden pembeli. Dengan demikian dapat diambil
rata-rata dari kedua presentase interpretasi keduanya yaitu 89,5% bahwa aplikasi yang telah dibuat
dapat bermanfaat untuk membantu sistem penjualan di Batik Puspa Kencana.<br>
<b><i>Kata kunci : android, penjualan, batik, teknologi informasi.</b></i></p>
