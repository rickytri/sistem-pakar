 <h3>Abstract</h3>
  <h3>Image Coloring Educative Mobile Game Programming with Adobe Flash CS5 </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Nowadays mobile devices from many developed. One application that can be
used to develop applications on mobile devices is Adobe Flash. Flash was chosen
because it is more user friendly, each job can be viewed directly on the Stage. Flashbased
mobile applications can also be in the package to a variety of mobile devices.
Nowadays it becomes a major convenience, many people now want a life in
hand. Mobile phone is no longer rare items possessed, of users. Not only adults and
teenagers, even children were already familiar with the mobile phone. One of the
applications that complement a mobile device is game. Dra. Entit Usidati, S.ST, a teacher
of Visual Communication Design explained that the game is the medium of the present
study, human nature has to learn everything faster visual-verbal so good game to get
involved in the process of education (educational games).
Based on the author wanted to make a mobile phone application based
educational games. This game will be titled “Mewarnai Gambar”. Here the children will be
invited to paint an image according to the color of their choosing. This game aims to
develop creativity and abilities of children.
<br>
<b><i>Keywords: Adobe Flash, Mobile Phone, Educational Game.</b></i></p>
