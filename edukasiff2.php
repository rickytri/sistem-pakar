  <h3>Rebana Traditional Musical Instrument Education Game with Android Basic</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>The traditional musical instrument tambourine is a traditional musical instrument that is usually used for
Islamic occasions accompanist. Although the traditional musical instrument tambourine instead of original
from Indonesia, but in its spread, this instrument has a characteristic of each area. But over time, this
instrument is likely to be abandoned. Society in general tend to prefer to the modern instrument. This is
supported by the lack of games about traditional musical instruments, while virtual games to traditional
musical instrument is very much. Android-based device in the era now is booming, both from the perspective
of the developer and the user. The making of this game using the method prototype. Games for learning a
traditional musical instrument tambourine-driven Android aims to create a learning game play traditional
musical instrument tambourine for android-based mobile, it also gives the knowledge about musical
instrument tambourine, how to play musical instruments and information on the tambourine. Creation in the
form of mobile aims to easy to carry anywhere. With the creation of this game allows users to interact
directly in the process of playing the tambourine in accordance with mekasnisme the real musical
instruments. The results of a survey addressed to 10 people from some quarters, gained satisfaction
rate of 82.4% which means very satisfied.<br>
<b><i>Keywords: game, traditional musical instrument, tambourine, Android</b></i></p>
