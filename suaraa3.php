 <h3>Abstract</h3>
  <h3>Wheeled Robot Built Programming with Voice Controller </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>One type of robots with special capabilities that interesting to develop is wheeled robot that using voice as
controller that utilize voice recognizing technology as the robot’s DC motor activator. The design of the robot use the
Arduino UNO R3 as controller, Easy Voice Recognizing module, Motor Driver as the DC motor driver, while wheel
system use DC motor as actuator for the mechanics. This research use C++ Arduino as the Programming Language, Easy
Voice Commander as the voice recorder and Matlab as the voice signal processor and to see the differences between the
voice signal that can or cannot be recognized. Testing at ideal condition show success rate is 80%, at noisy condition the
testing show 40% success rate. Testing system performance at ideal condition show success rate is 87,5%, at noisy
condition the testing show 37,5% success rate.<br>
<b><i>Keyword : Arduino UNO R3, Matlab, Robot, Voice Recognizing</b></i></p>
