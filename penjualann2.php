<div class="container-fluid bg-2 text-center">
 
  <p>The development of information technology so rapidly that can covering every aspects of human's life. Starting with a social media trend that grow so rapid in the last five years.
With that growth, Information technology is no longer just being communication media, but also can be a tool to expand business.
CV Khatulistiwa is one of the many snacks companies in Indonesia.
To be able to globally compete, they need a tool to reach bigger market.
With IT, this company is making a web portal as a tool to promoting their brands and products. This research is to design those application that can help CV Khatulistiwa to reach bigger market.
This application is using Prototype method, so that can be useful for this company.
