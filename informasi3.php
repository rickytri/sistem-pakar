<div class="container-fluid bg-2 text-center">
 
  <p>Sistem informasi pada SMPN 2 Cineam masih dilakukan secara manual. Dalam
pengolahan data dan penyampaian informasi masih secara konvensional. Dengan sistem
yang ada sekarang dirasakan masih banyak kekurangan.
Tugas akhir ini bertujuan untuk membangun sebuah media informasi berbasis web
dalam bentuk forum diskusi di SMPN 2 Cineam untuk memfasilitasi dan mempermudah
dalam penyampaian informasi baik akademik atau non akademi, dimana siswa dan guru
bisa mendapatkan informasi sekolah dengan mudah dan cepat. Selain itu dengan berbasis
web maka informasi dapat diakses dalam waktu dan tempat yang tidak ditentukan. Pada
system ini menu hanya dapat diakses oleh user tertentu yaitu siswa, guru dan
administrator.
Dalam pembuatan aplikasi ini penulis menggunakan PHP dan MySQL. Penulis juga
menganalisa sejauh mana media informasi ini dapat membantu pihak sekolah dalam
proses belajar mengajar dan penyampaian informasi, baik pada pihak administrasi sekolah,
guru maupun siswa.
<br>
<b><i>Kata kunci : SMPN 2 Cineam, Media Informasi berbasis web, Forum Diskusi, PHP
dan MySQL.</b></i></p>
