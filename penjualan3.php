<div class="container-fluid bg-2 text-center">
 
  <p>PT SIMTEX Mechatronic Indojaya (SMI) adalah perusahaan yang bergerak dalam bidang<Br>
perdagangan <i>hardware</i> yang memberikan solusi dalam bidang tata udara, <br>
peredam getaran (<i>vibration control</i>) dan peredam suara (<i>noise control</i>)<br>
serta memberikan jasa <i> maintenance </i> dari berbagai <i>hardware</i> yang dijual. Namun,<br>
dalam kegiatan penjualannya PT. SMI masih menggunakan cara menempel brosur di tempat-tempat<br>
umum, <i>door to door</i>, dan dengan cara  menelpon instansi yang kira-kira membutuhkan <Br>
<i>hardwarenya</i> sehingga berakibat pada kurang meningkatnya angka penjualan <i>hardware.</i> <br>
Peningkatan angka penjualan hardware tidak sesuai deret hitung, setiap bulannya mengalami penurunan. Data<Br>
ini dapat dilihat di bagian lampiran II. Selain itu dalam memproses dan menyimpan data pemesanan pelanggan,<Br>
PT. SMI belum menggunakan cara terkomputerisasi, yaitu dengan mencetak faktur pemesanan yang telah di proses,<br>
kemudian disimpan di bagian arsip, cara seperti ini mengakibatkan data pemesanan hardware oleh pelanggan tidak terorganisir<Br>
dengan baik dan sering hilangnya faktur pemesanan yang sudah selesai di proses. akibatnya, bagian sales marketing tidak dapat<br>
mendata secara rinci hardware-hardware apa saja yang paling diminati oleh pelanggan dan berapa banyak hardware yang telah berhasil<br>
terjual tiap bulannya. Dalam merumuskan solusi bagi permasalahan tersebut diatas, peneliti mengusulkan sebuah sistem yang dapat <Br>
memasarkan hardware dari PT. SMI melalui media internet dan dapat menyimpan dan memproses data pemesanan pelanggan agar lebih efisien.<Br>
Sistem Penjualan dikembangkan menggunakan bahasa pemrograman PHP dengan MySQL sebagai basis datanya. Metode pengembangan sistem yang digunakan<Br>
adalah <i>Rapid Application Development</i>(RAD) yang berbasiskan object oriented dan <i>Microsoft Visio 2003</i> sebagai editor perancangan<Br>
sistemnya. Diharapkan dengan adanya sistem penjualan ini dapat membantu sebagai media penjualan <i>hardware</i> PT SMI. Sedangkan untuk proses<br>
pemesanan <i>hardware</i> dan penyimpanan datanya, sistem penjualan ini diharapkan dapat memproses dan menyimpan data pemesanan <i>hardware</i><Br>
dari pelanggan agar lebih efisien.<br><br><br>
<b><i>Kata Kunci: Sistem Informasi Penjualan, Web, RAD</b></i></p>
