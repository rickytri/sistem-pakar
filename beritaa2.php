 <h3>Abstract</h3>
  <h3>Android Basic News Portal Mobile Application Ganto.or.id</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>The development of mobile communication technology leads people to use technology not
only as a communication tool, but also as a tool to find a variety of information, and news.
Today's society considers mobile communications media as a very practical needs in its use and
has high mobility. Many news portal now also includes news portal applications for mobile
devices. Ganto UNP news portal does not provide the application which led to the news portal
infrequently accessed through mobile devices because of the view that is not user-friendly when
accessed through the device. To overcome this problem, it is necessary to design a mobile
application for Ganto news portal. So that mobile device users no longer need to access the
Ganto news portal through a web browser. Simply by using the mobile application. This
application was designed with the Java programming language, Eclipse as an IDE, Eclipse ADT
as a plugin on android, and the Android SDK for Android application development, as well as
the AVD emulator to run the android system. Programming language used to build web-service
is PHP programming language and MySQL DBMS that has owned news portals Ganto as data
storage server. By utilizing the Android mobile device technology, successfully obtained an
application that can accommodate the needs of people who have a high level of mobility to
access information on Ganto news portals anytime and anywhere effectively and efficiently.<br>
<b><i>Keywords : News Portal, Ganto, Android, web-service.</b></i></p>
