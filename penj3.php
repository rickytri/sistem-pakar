<div class="container-fluid bg-2 text-center">
 
  <p>Toko Tas Fani merupakan sebuah toko tas yang terletak di BTC Lt.1 Pangkalpinang Bangka Belitung. 
  Toko Tas Fani menual berbagai macam jenis tas seperti tas ransel, tas bepergian, tas kerja, dll.
Semua transaksi jual beli dilakukan secara manual. Pembeli harus datang lanngsung ke toko untuk membeli barang. 
Hal ini tidak efisien. M-commerce (mobile commerce) merupakan sistem perdagangan yang dijalankan melalui media yang 
mudah dibawa seperti telepon pintar, PDA, dll. Aplikasi ini berbasis android dengan menggunakan jenis bahasa 
pemrograman Java, MYSQL sebagai databse, dan PHP sebagai webserver. M-commerce menjadi solusi permasalahan Toko Tas 
Fani terkait dalam hal media perdagangan, konsumen sekarang dapat melihat-lihat dan membeli produk yang dipasarkan 
Toko Tas Fani tanpa harus datang langsung ke toko. Dapat disimpulkan bahwa menggunakan aplikasi mobile untuk traksaksi 
seperti ini dapat dilakukan dengan lebih mudah, efisien dan dapat menjangkau area yang lebih luas.
<br>
<b><i>Kata Kunci : M - Commerce , Android , Mobile Application, Toko Tas Fani</b></i></p>
