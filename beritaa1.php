  <h3>Mobile News Application on Suaramerdeka.com using Android Platform</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Suaramerdeka is the largest newspaper in Central Java. Besides using the papers also has an online news
portal that suaramerdeka.com. Currently the suaramerdeka.com visitor using mobile browser is very large, so
need to be developed into a smartphone app to facilitate the general public in accessing news quickly and
practically. This application is expected to be delivered to the user and can be used as much as possible to
all people. This application is designed to keep track of current mobile that android and phonegap. The main
features of this application provides the latest news updates occurring within and outside the country, there is
also a special feature that is reporting the incident to the suarmerdeka. It can be concluded that the
application is in compliance with the main feature in the delivery of news as found on suaramerdeka portals a
nd also makes it easy to access the facility suaramerdeka news portal.<br>
<b><i>Keyword : Mobile Application. Android, Phonegap
</b></i></p>
