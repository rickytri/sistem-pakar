<div class="container-fluid bg-2 text-center">
 
  <p>Keperluan sistem monitoring saat ini semakin mempermudah pengguna dalam melakukan monitoring
tempat, seperti rumah. Kebutuhan akan sistem monitoring, umumnya akan membutuhkan perangkat
keras seperti komputer desktop (PC) dan kamera sebagai perangkat penunjang. Namun biaya untuk
perangkat keras dan pengeluaran biaya listrik untuk penggunaan PC desktop pada sistem monitoring
membutuhkan biaya yang cukup mahal. Penggunaan dari Mini PC Raspberry Pi sebagai pengganti
PC desktop, adalah untuk mendapatkan biaya pembangunan sistem serta penggunaan biaya listrik
yang lebih murah. Raspberry Pi yang digunakan memiliki ukuran perangkat yang kecil, serta
processor ARM 700MHz, kapasitas RAM sebesar 512MB, dan kamera eksternal 5MP dari jenis
PiNoir. Berdasarkan hasil pengujian yang dilakukan menunjukkan bahwa kinerja Raspberry Pi
didapatkan penggunaan CPU mencapai 100% dari kapasitas dan penggunaan RAM mencapai 32%
dari kapasitas yang tersedia. Walaupun dengan beban pemrosesan yang tinggi, Raspberry Pi tetap
mampu menggantikan PC desktop dalam melakukan fungsi monitoring, sehingga tujuan
pembangunan sistem serta penggunaan biaya listrik yang murah dapat tercapai.<br>
<b><i>Kata Kunci :Mini PC, Raspberry Pi, Monitoring</b></i></p>
