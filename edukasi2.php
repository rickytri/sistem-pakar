<div class="container-fluid bg-2 text-center">
 
  <p>Saat kini, bahasa Mandarin semakin banyak diminati orang. Di Indonesia, penggunaan bahasa
internasional setelah bahasa Inggris, adalah bahasa Mandarin (Sylvie Tanaga, 2010). Bahasa Mandarin
sebagai mata pelajaran peminatan jurusan bahasa dalam kurikulum 2013 yang dapat dipelajari siswa
SLTA. Karakteristik bahasa Mandarin yang bersifat unik baik huruf, tulisan, ucapan dan intonasinya,
selayaknya proses belajar mengajarnya lebih interaktif, didukung perangkat multimedia dan tidak selalu
berada dalam suatu kelas. Alternatif pengajaran bahasa Mandarin akan lebih menarik, dengan
memberikan permainan interaktif serta ditopang dengan fasilitas edukasi offline/online yang dapat
diakses melalui perangkat teknologi informasi. Kesempatan siswa menggunakan komputer atau internet,
memudahkan berkomunikasi secara online dengan teman dan gurunya, atau orang lain untuk lebih
memahami bahasa Mandarin. Memenuhi kebutuhan tersebut, perlu dirancang perangkat lunak aplikasi
sebagai media pembelajaran bahasa Mandarin. Rancang bangun aplikasi sistem pembelajarannya, dapat
dikembangkan dengan menggunakan paket aplikasi open source e-learning yang bersifat gratis. Metode
perancangannya dengan pendekatan daur hidup pengembangan sistem yang dicapai melalui beberapa
tahapan kegiatan mulai dari perencanaan, analisa, perancangan, sampai penerapan aplikasi sistem
pembelajaran yang dapat diakses melalui website. Luaran dari hasil penelitian berupa sistem elearning,
yang diharapkan dapat dijadikan sebagai alternatif media belajar mengajar bahasa Mandarin.<br>
<b><i>Kata Kunci: Pembelajaran, interaktif, elearning, open source </b></i></p>
