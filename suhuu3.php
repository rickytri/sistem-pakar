 <h3>Abstract</h3>
  <h3>Temperature Controller System and Arduino Basic for Humidity Room using DHT22 Sensor and Infrared Passive (PIR) </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>We have been successfully to create a Control System Temperature and Air Humidy Monitoring Room Based on Arduino
Uno with DHT22 Sensor and Passive Infrared (PIR). DHT22 Sensor be used as a measurement of temperature and air
humidity in the room and Passive Infrared Sensor as a tool of detection the movement of people in a room. In this
experiment, carried out tests of the DHT22 sensor and digital thermometer with a type AZ-HT-02 to room temperature with a
hot air given through a hairdryer for 3 minutes with a span of per 10 seconds. In this test, the obtained data is that the room
temperature is which measured by DHT22 sensor have an average difference of 0.93 against digital thermometer (as a
calibration). In this test also obtained DHT22 sensor more sensitive Rrelative Humidity (RH), this is caused when given the
hot air, humidity measured more rapid adjustment and yet different with digital temperature was slow to adjust air humidity
in the room. This device using an Arduino Uno board, a Liquid Crystal Display (LCD) module and Real Time Clock (RTC)
as a display of the temperature and air humidity in real time. This device is connected to a fan that can stabilize the room
temperature are considered less convient to do activities in the room.<br>
<b><i>Keywords: Air Humidity, Arduino Uno, DHT22 Sensor, Passive Infrared Sensor, Temperature
</b></i></p>
