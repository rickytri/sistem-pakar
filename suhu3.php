  <h3>Sistem Kendali Suhu dan Pemantauan Kelembaban Udara Ruangan Berbasis Arduino UNO dengan Menggunakan Sensor DHT22 dan Passive Infrared (PIR)</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Telah berhasil membuat Sistem Kendali Suhu dan Pemantauan Kelembaban Udara Ruangan Berbasis Arduino Uno dengan
Menggunakan Sensor DHT22 dan Passive Infrared (PIR). Sensor DHT22 digunakan sebagai alat ukur suhu dan kelembaban
udara ruangan dan sensor Passive Infrared (PIR) sebagai pendeteksi adanya pergerakan manusia dalam suatu ruangan. Dalam
percobaan ini dilakukan pengujian pada kepekaan sensor DHT22 dan termometer digital model AZ-HT-02 terhadap suhu
ruangan dengan diberikan udara panas melalui hairdryer selama 3 menit dengan rentang waktu per 10 detik. Dalam
pengujian ini diperoleh data bahwa suhu ruangan yang diukur oleh sensor DHT22 memiliki rata-rata selisih 0.93 terhadap
termometer digital model AZ-HT-02 (sebagai kalibrasi). Dalam pengujian ini pula didapatkan sensor DHT22 lebih peka
terhadap Relative Humidity (RH), dikarenakan ketika diberikan udara panas tersebut kelembaban yang terukur lebih cepat
mengalami penyesuain dan berbeda dengan termometer digital AZ-HT-02 yang lambat untuk menyesuaikan kelembaban
udara ruangan. Perangkat ini menggunakan board Arduino Uno, sebuah modul Liquid Crystal Display (LCD) dan modul
Real Time Clock (RTC) sebagai tampilan suhu dan kelembaban udara secara real time. Perangkat ini terhubung dengan
sebuah kipas angin yang dapat menstabilkan suhu ruangan ketika suhu tersebut dinilai kurang nyaman untuk melakukan
berbagai kegiatan didalam ruangan.<br>
<b><i>Kata Kunci : Arduino Uno, Kelembaban Udara, Sensor DHT22, Sensor Passive Infrared, Suhu.
</b></i></p>
