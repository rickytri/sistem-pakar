<div class="container-fluid bg-2 text-center">
 
  <p>Keterbatasan perangkat pengolah data adalah bentuknya yang cukup besar sehingga
kurang flexible dalam fungsi mobile. Raspberry Pi adalah sebuah komputer papan tunggal
(single-board computer) atau SBC berukuran kartu kredit. Raspberry Pi telah dilengkapi dengan
semua fungsi layaknya sebuah komputer lengkap, menggunakan SoC (System-on-a-chip) ARM
yang dikemas dan diintegrasikan diatas PCB. CCTV merupakan salah satu perangkat yang
diharapkan dapat menekan angka kriminalitas pada area publik. Namun kendala mahalnya
biaya instalasi (baik listrik atau network) dan terbatasnya mobilitas penempatan alat menjadikan
penggunaan perangkat ini belum maximal. Penelitian ini menawarkan solusi alternatif untuk
membenahi permasalahan di atas. Pengembangan Prototipe Public Monitoring System
Menggunakan Raspberry Pi merupakan sub penelitian awal dari grand research Integrasi
Aplikasi Cerdas Public Monitoring System. Output yang akan dicapai adalah terbangunnya
perangkat untuk monitoring sistem yang memiliki design mobilitas yang dapat ditempatkan di
berbagai tempat. Dengan penelitian ini diharapkan mampu memberikan wacana pemanfaatan
Raspberry Pi untuk membangun monitoring sistem yang murah dan mendukung program hemat
listrik pemerintah.<br>
<b><i>Kata Kunci : : raspberry pi, monitoring system, cctv, sistem cerdas, mobility</b></i></p>
