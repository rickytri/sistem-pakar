<div class="container-fluid bg-2 text-center">
 
  <p>Word Puzzle Game-Based Applications Android is an application that type of game Word Puzzle that serves to entertain and also
provide knowledge about the names of animals in English and a brief explanation. The application is built using the Java
programming language and using Sqlite database. Development is done by Eclipse as the main tool, and Adobe Photoshop as a
supporting tool. The process of making the entire mechanism in the game is done with Eclipse, while the design process and display
the object is done with Adobe Photoshop, and the testing process is done on a smartphone Lenovo A800. In this application, there are
two main functionality of the play and view information. At first functionality, users will be invited to correct the names of animals
that typo. In addition, players will also be invited to unlock the achievement so that the user can see the information in the form of
images of animals and also the explanation that all belong to the second functionality.<br>
<b><i>Keywords: Games, Word Puzzles, Android.</b></i></p>
