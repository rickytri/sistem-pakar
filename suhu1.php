<div class="container-fluid bg-2 text-center">
 
  <p>Akhir-akhir ini sering terjadi kebakaran yang disebabkan oleh beberapa faktor termasuk
faktor kelalaian manusia. Proses datangnya kebakaran selalu tanpa dapat diprediksi terlebih
dahulu. Umumnya kebakaran diketahui apabila keadaan api sudah mulai membesar atau asap
sudah mengepul keluar dari gedung itu. Penelitian ini bertujuan untuk merancang alat
pendeteksi kebakaran menggunakan sensor suhu dan sensor asap yang dapat mendeteksi
adanya kebakaran secara dini. Perancangan sistem dimulai dari rangkaian sensor suhu, sensor
asap, konverter tegangan analog ke digital dengan ADC 0809, pengendali sistem dengan
mikrokontroler AT89S52, dan alarm sebagai indikator terjadinya kebakaran. Hasil penelitian
menunjukkan bahwa prototipe sistem pendeteksi kebakaran yang dirancang dapat mendeteksi
kebakaran berdasarkan informasi dari sensor suhu dan asap, dan mengaktifkan alarm sebagai
indikatornya akan berbunyi.<br>
<b><i>Kata kunci: sensor suhu, sensor asap, ADC, mikrokontroler AT89S52 
</b></i></p>
