<div class="container-fluid bg-2 text-center">
 
  <p>Kehilangan merupakan pengalaman yang pernah dialami oleh setiap individu selama
rentang kehidupannya dan cenderung akan mengalaminya kembali walaupun dalam bentuk yang
berbeda. Dalam Perkembangan masyarakat, proses pencarian kehilangan sedikit demi sedikit mulai
berkembang. Dimana individu yang mengalami proses ini mempunyai keinginan untuk
memberitahukan informasi kehilangan kepada orang lain. Kemajuan teknologi informasi dapat
dimanfaatkan sebagai media untuk mengumumkan informasi kehilangan. Namun belum ada wadah
yang mengkhususkan menampung data dan informasi kehilangan. Maka dari itu, muncul ide untuk
membangun apikasi jejaring sosial untuk berbagi informasi kehilangan. Dalam membangun aplikasi
jejaring sosial digunakan suatu metode pengembanagan yaitu The Web Modelling Language
(WebML). Tahapan proses meliputi proses analisa kebutuhan, perancangan sistem, implementasi
dan pengujian. Adapun untuk proses analisis kebutuhan didasarkan pada hasil wawancara dan
observasi dengan para pengguna jejaring sosial. Aplikasi jejaring sosial ini akan menggunakan
teknologi SIG sehingga informasi dapat dilihat secara visual (peta) dan informasi kehilangan
ditampilkan berdasarkan letak hilangnya suatu objek (harta).<br>
<b><i>Kata Kunci – Informasi Kehilangan, Aplikasi Jejaring Sosial, WebML, SIG</b></i></p>
