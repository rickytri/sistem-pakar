 <h3>Abstract</h3>
  <h3>DESIGN OF ALUMNI SEEKER SOCIAL NETWORK WEBSITE FOR SEKOLAH TINGGI TEKNOLOGI GARUT </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>The purpose of this research is web designing about alumni seeker social network website for Sekolah Tinggi Teknologi Garut.
This website uses Evolutionary Web Development method (Ginige and Murugessan, 2001). The result of this research is the design of a new system alumni seeker and portal with many useful menus either for the campus' needs or for the alumni themselves.
To support campus' needs, the system have to process and seek almuni's data, such as data member facility, data recommendation facility, and alumni's report. Data recommendation facility is the main facility with "1 member register then 10 alumni's data alumni covered".
Each registered alumni can recommend alumni's data which they know to the system, so the campus can follow up the data to seek more alumni's data. 
While some other facilities are designed to meet the needs of alumni including job vacancy news, campus news, scholarship news and reunion news.
<br>
<b><i>Keywords : Social networking, Alumni seeker, Evolutionary web development.</b></i></p>
