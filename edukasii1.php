  <h3>Web Basic E-learning Education System </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Education development will take effect significantly to nation development, especially knowledge and technology development. Technology effort implementation especially in comunication information technology in the field of education one of it is education system using internet media or usually called with e-learning. E-learning give easiness to gain education material without pay, sharpen skills with questions trial and it’s easier to interact with teacher wherever and whenever.
Development method which writer used is waterfall method on this research. Waterfall method is method which used systematic approach and sequence from level system needs define until maintenance. Waterfall method consist of 6 phase, such as requirement specification, architectural design, coding, integration & testing, operation & maintenance.
The result of this research is more than 80% respondents said this system is easy to use, this system has complete menu and this system can be used to self study at home, and more than 75% respondent said this system can escalate study quality. With that conclusion, the purpose of this research has been achieved.
<br>
<b><i>Keyword: Education System, E-learning, Web.</b></i></p>
