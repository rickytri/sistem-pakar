<div class="container-fluid bg-2 text-center">
 
  <p>Pembelajaran anak usia dini adalah pendekatan pengembangan yang diperlukan oleh anak-anak dari 
  lahir sampai umur delapan tahun melalui ketentuan edukasi stimulus untuk membantu pertumbuhan dan
  perkembangan fisik dan spiritual sehingga anak-anak siap untuk memasuki edukasi selanjutnya. Untuk membantu 
  pertumbuhan dan perkembangan anak kemudian dibangun game edukasional. Game edukasional adalah game yang 
  membantu pembelajaran dan materi pembelajaran dimasukan ke permainan. Game edukasional yang akan dikembangkan 
  adalah game untuk melatih anak usia dini khususnya untuk anak berumur 3 sampai dengan 8 tahun sehingga mampu membaca, menulis 
  dan menghitung dengan benar. Game edukasional ini disebut dengan Game Calistung. Game Calistung edukasional 
  diterapkan ke sistem operasi android. Dan untuk keperluan presentasi Calistung menggunakan Linear Congruent Method (LCM). 
  Linear Congruent Method (LCM) adalah salah satu metode untuk generator nomor acak. Dimana penentuan LCM konstan (a, c dan m)
  akan menentukan bisa atau tidak nomor acak diperoleh seakan pengulangan tidak terjadi.<br>
