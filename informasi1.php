<div class="container-fluid bg-2 text-center">
 
  <p>Posyantek sebagai lembaga yang didirikan oleh pemerintah adalah pos pelayanan teknologi
pada tingkat kecamatan dalam rangka memberikan pelayanan teknis, informasi, promosi dan
pemasaran berbagai jenis spesifikasi peralatan teknologi tepat guna yang dibutuhkan oleh
masyarakat maupun dalam rangka meningkatkan kemampuan sumberdaya manusia di perdesaan
(Kepmendagri No. 18 tahun 1992 Tentang Pemasyarakatan dan pemanfaatan Teknologi Tepat Guna
di Perdesaan). Keberadaan lembaga ini sudah bayak tak terkecuali Posyantek Kecamatan Tegal
Timur. Namun sepanjang berdirinya lembaga ini belum optimal dalam pengelolaannya, sehingga
terkesan lembaga hanya tinggal nama, sehingga perlu revitalisasi fungsi dan peran lembaga ini,
salah satu fungsi posyantek adalah Pusat pelayanan teknis pemberian informasi dan promosi serta
pemasaran jenis spesifikasi teknologi tepat guna dan mempunyai salah satu tujuan adalah
memberikan pelayanan tentang pengenalan jenis spesifikasi teknologi tepat guna yang akan
dikembangkan dan disebarluaskan kepada masyarakat dan mempermudah masyarakat untuk
mengembangkan usaha dengan mendayagunakan teknologi tepat guna. Metode yang digunakan
untuk membangun sistem adalah metode Object Oriented Analysis and Design (OOAD). Metode
ini menghasilkan pemodelan sistem untuk tahap requirement, analysis, dan design selanjutnya
diimplementasikan menggunakan. Hasil yang dicapai dalam penelitian ini adalah pembuatan sitem
informasi sebagai media media Informasi dan Media Pemasaran akan membantu efektifitas fungsi
dan tujuan didirikannya Posyantek dengan alamat informasi pemasaran berbasis web yaitu
http://posyantek.wordpress.com.<br>
<b><i>Kata kunci: sistem informasi pemasaran, Object Oriented Analysis and Design (OOAD).</b></i></p>
