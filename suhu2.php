  <h3>Rancang Bangun Prototipe Monitoring Suhu Tubuh Manusia Berbasis O.S Android menggunakan Koneksi Bluetooth</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Tanda-tanda vital kesehatan manusia dapat
diketahui dari suhu tubuh, pernapasan, tekanan darah, dan
denyut nadi. Dalam tugas akhir ini, dirancang sebuah prototipe
monitoring suhu tubuh manusia berbasis android menggunakan
koneksi bluetooth dengan performansi dalam bentuk jaket yang
terdiri dari tiga buah titik pengukuran. Langkah-langkah yang
dilakukan untuk merancang sistem pengukuran suhu tubuh
adalah dengan mendesain sistem perangkat keras dan perangkat
lunak. Sensor yang digunakan adalah IC-LM35 yang tersebar di
tiga titik pengukuran. Sensor ini sebagai pendeteksi perubahan
suhu tubuh, kemudian hasil perubahan suhu akan dibaca dan
diproses pada mikrokontroler ATMega8a-pu. Setelah data
terproses, selanjutnya data akan dikirimkan ke perangkat
android melalui koneksi bluetooth. Setelah dilakukan
pengukuran dan analisis data, dapat diketahui bahwa alat ini
memiliki respon dinamis settling time sebesar 5 menit. Jarak
maksimum koneksi perangkat adalah 35 meter, dan kemampuan
baterai sebesar 9 volt selama 90 menit. Alat ini memiliki nilai
eror akurasi sebesar 0.29150C, eror presisi sebesar 0.8°C untuk
sensor A (mulut), dan nilai eror akurasi sebesar 0.4149°C, eror
presisi sebesar 0.2974°C untuk sensor B (ketiak kanan), dan nilai
eror akurasi sebesar 0.3846°C, eror presisi sebesar 0.5956°C
untuk sensor C (ketiak kiri).<br>
<b><i>Kata Kunci—suhu, kesehatan, mikrokontroler, bluetooth, android.
</b></i></p>
