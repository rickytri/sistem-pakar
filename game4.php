  menjelaskan bahwa game adalah pelajaran medium sekarang ini, manusia belajar segala sesuatu dengan cepat baik visual maupun 
  verbal sehingga game menjadi  berhubungan dengan proses edukasi (game edukasional). Berdasarkan pengarang diinginkan untuk 
  membuat telepon genggam berdasarkan game edukasional. Game ini akan diberi judul “Mewarnai Gambar”. Disini anak-anak akan 
  diundang untuk melukis gambar berdasarkan warna yang mereka pilih. Game ini bertujuan untuk mengembangkan kreativitas dan kemampuan anak-anak.
