 <h3>Abstract</h3>
  <h3>Design of GPS based Campus' Social Networking Applications On Android Smartphone</h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>Campus is a place where geotagging social networking app geotagging services is very needed. Geotagging service on campus will greatly help the academic activities, such as finding the lecturer's / student's location quickly and accurately. 
The problem now is this kind of app doesn't provide automatic geotagging.
This paper describes the internal campus' social networking applications based on android which can automatically  update the user's location.
The test results on web server is showing that the higher transfer rate's value, the more request that the server can handle in one second.
<br>
<b><i>Keywords: social networking; Microblogging; Autogeotagging; Android; Request per second</b></i></p>
