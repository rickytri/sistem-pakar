<div class="container-fluid bg-2 text-center">
 
  <p>Gas merupakan unsur penting di lingkungan, yang dapat menjadi salah satu parameter untuk
menentukan kondisi lingkungan bersih atau tercemar. Sifat gas yang transparan menyebabkan sulit
untuk mengetahui kondisi lingkungan tersebut, apakah masih bersih atau sudah tercemar gas
berbahaya. Pada penelitian ini dibuat suatu robot kontrol jarak jauh yang dilengkapi dengan dua
sensor gas karbon monoksida dan ammonia. Pengendalian robot tersebut berbasis mikrokontroler
ATmega32 dan diakses menggunakan tablet Android dengan media bluetooth sebagai interface.
Metode logika fuzzy Sugeno digunakan untuk memutuskan hasil pembacaan sensor pada robot,
apakah kondisi lingkungan aman atau berbahaya. Berdasarkan hasil pengujian didapatkan bahwa
arah pergerakan robot sudah sesuai 100%, jarak jangkau maksimum bluetooth pada kondisi NLOS
(Non Line of Sight) adalah 27 meter, dan keputusan fuzzy sudah bekerja sesuai yang diharapkan.<br>
<b><i>Kata kunci : robot, karbon monoksida, amonia, fuzzy Sugeno, NLOS</b></i></p>
