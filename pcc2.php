 <h3>Abstract</h3>
  <h3>Monitoring System Using Mini PC Raspberry Pi </h3>
</div>

<div class="container-fluid bg-2 text-center">
 
  <p>This day monitoring system needs are making it easier for user to do place monitoring, such as home. Monitoring system needs, generally will need hardware such as desktop computer (PC) and camera as supported device. But the fee for hardware and electrical for PC desktop are expensive enough. The using of Mini PC Raspberry Pi as PC Desktop replacement, is for having system build fee and electrical fee is more cheaper. Raspberry Pi which will be used has small device size, and processor ARM 700MHZ, RAM capacity 512MB, and external camera 5MP from PiNoir series. Based on test result which be done is pointing that Raspberry Pi works that CPU reach 100% from capacity and RAM use reach 32% from available capacity. Although it has high processing load, Raspberry Pi still can change PC desktop on high monitoring function, so system build and cheaper electrical fee can be reached.<br>
<b><i>Keywords: Mini PC, Raspberry Pi, Monitoring</b></i></p>
