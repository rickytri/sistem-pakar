<div class="container-fluid bg-2 text-center">
 
  <p>News reader is a software or web service that serves to display a summary of the latest
posts. RSS is typically used on websites that update content dynamically. RSS is an open
method for collecting and pooling the content of the pages of a website. The method used in the
making of this application is the grapple. The programming language used is Java and php.
While building a database using mySQL.This application is useful to facilitate mobile phone
users to know the latest news. In this application there are news categories that can be selected
by the user, namely the category of politics, economics, sports, technology, education and
entertainment. Featured news picked up from a local news portal website rss Indonesia. Users
can view the details of the news and share news via facebook and twitter. There was also the
news search feature that utilizes existing news search feature on the website news provider.<br>
<b><i>Keywords : Application, News Reader, RSS</b></i></p>
