  <p> This snake robot works using DC motor as its movers and microcontroller as main controller. Microcontroller circuit become main controller for all another electronic component, which consist of DC motor, detector metal sensor, ultrasonic sensor, limit sensor, potensiometer and webcam.
Active snake robot body is controlled by a microcontroller and passive body which has installed detector metal sensor. On head ultrasonic and limit sensor and webcam have installed. Microcontroller on head assemblied to each of microcontroller in another body.<br>
